// 2. What happens when you divide a double variable by 0?
// It will return Infinity.
// 3. What happens when you overflow an int variable, that is, set it to a value beyond its range?
// It will throw a System.OverflowException at runtime.
// 4. What is the difference between x = y++; and x = ++y;?
// x = y++; will assign the value of y to x and then increment y by 1. x = ++y; will increment y by 1 and then assign the value of y to x.
// 5. What is the difference between break, continue, and return when used inside a loop statement?
// break will exit the loop, continue will skip the rest of the loop and start the next iteration, and return will exit the method.
// 6. What are the three parts of a for statement and which of them are required?
// The three parts of a for statement are the initialization, condition, and iterator. The initialization and condition are required.
// 7. What is the difference between the = and == operators?
// = is the assignment operator and == is the equality operator.
// 8. Does the following statement compile? for ( ; true; ) ;
// Yes, it will compile. It is an infinite loop.
// 9. What does the underscore _ represent in a switch expression?
